    private void lActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lActionPerformed
        try{
            String s1 = e.getText();
            String s2 = String.valueOf(p.getPassword());
            ResultSet rs = db_files.dbconnect.st.executeQuery("select * from admin_login "
                    + "where email='"+s1+"' and password='"+s2+"'");
            if(rs.next()){
                String n = rs.getString("name");
                new registernewstudent(n).setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Invalid Entries!!");
            }
        }catch(Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_lActionPerformed

